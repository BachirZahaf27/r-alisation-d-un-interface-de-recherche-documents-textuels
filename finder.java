    private void auteur_serachActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_auteur_serachActionPerformed
        // rechercher un auteur
        String auteur_nom=txt_auteur.getText();
        conn=database.java_db();
        try {
            //le travaill de auteur
        String sql="SELECT * FROM document WHERE auteur LIKE '%"+auteur_nom+"%'";
         stmt=conn.prepareStatement(sql);
         rs=stmt.executeQuery();
         table_doc.setModel(DbUtils.resultSetToTableModel(rs));
         
        //la cart de auteur 
         String sql2 ="select * from auteur where nom_auteur=? ";
        
            stmt=conn.prepareStatement(sql2);
            stmt.setString(1,txt_auteur.getText());
            rs=stmt.executeQuery();            
            
            String chemp=rs.getString("image");
            lb1.setText(chemp);
            lb1.setVisible(false);            
            ImageIcon imageIcon = new ImageIcon(new ImageIcon(chemp).getImage().getScaledInstance(lbl_img.getWidth(), lbl_img.getHeight(), Image.SCALE_SMOOTH));
            lbl_img.setIcon(imageIcon);
            
            String add1 =rs.getString("ID");
            txt_id.setText(add1);
            
            String add2 =rs.getString("nom_auteur");
            txt_nom.setText(add2);
            
            String add3 =rs.getString("prenom_auteur");
            txt_prenom.setText(add3);
            
            String add4 =rs.getString("bio_auteur");
            txt_bio.setText(add4);
            
             
            
            
         
         
         
        } catch(Exception e)

        {
             JOptionPane.showMessageDialog(null, e);

        }
        finally {
            
            try{
                rs.close();
                stmt.close();
                
            }
            catch(Exception e){
                
            }
        
        }
        
        
        
        
        
        
    }//GEN-LAST:event_auteur_serachActionPerformed

